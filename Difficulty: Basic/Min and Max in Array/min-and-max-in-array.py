class Solution:
    def get_min_max(self, arr):
        maxi = arr[0]
        mini = arr[0]
        for i in range(len(arr)):
            if arr[i]>maxi:
                maxi = arr[i]
            if arr[i]<mini:
                mini = arr[i]
        return mini,maxi

